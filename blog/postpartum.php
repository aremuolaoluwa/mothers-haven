                        <div class="postpartum-article-flex-one-two">
                            <p>As women, we feel so conflicted throughout our lives. It begins with who 
                                our best friend will be, and transitions to which college we will 
                                choose, or which career path to follow
                            </p>
                            <h1>“But none have caused me as much tug and pull as trying to find 
                                balance in motherhood”
                            </h1>
                            <p>It’s a daily up and down of feeling joy and frustration all in the same minute. 
                                I find myself counting down the seconds until bedtime and rushing through it, 
                                because I’m exhausted and drained and just generally frustrated that they 
                                need another sip of water, and just one more kiss.
                            </p>
                            <p>But fifteen minutes after they are fast asleep I find myself missing them, 
                                and feeling a weight of guilt that I didn’t enjoy that moment. “It goes 
                                by fast”, is what the empty nesters will tell you, and listen, we moms 
                                know this advice to be true, because we have a 1-year old who turned 
                                6 in the blink of an eye, but when you are in the thick of it, that 
                                advice is a hard pill to swallow.
                            </p>
                            <h2>But it will get easier, right?</h2>
                            <p>As I was in the trenches of the newborn stage with my first, I remember 
                                wondering how much easier life with a kid was going to be once they 
                                “needed” me less, but then I realized, with a heavy heart, they wouldn’t 
                                ever need me less, they would need me differently, always.
                            </p>
                            <p>Even so, when baby number two came, I still wished for the next stage, 
                                the one that would frustrate me less, the one that would be better than 
                                the one I was currently in. With baby number three, I tried so very hard 
                                to be present, I would lie beside him to snuggle, but then the guilt would 
                                set in, was I giving enough to the big kids, to my hubs, to myself?
                            </p>
                            <h2>Tug. Pull.</h2>
                            <p>I still tried to enjoy each stage as it came to me. Midnight feedings with 
                                this one, potty training with that one, teaching reading to my oldest… but then 
                                that tug and pull grabbed me again.
                            </p>
                            <p>I began wondering when my hubby and I would get time to just sit on the couch on 
                                a Sunday and read a book while the kids played, instead of furiously cleaning up 
                                from one mess to the next and realizing that by the time you got all three kids 
                                down for a nap, you only had 30 minutes, at best, to yourself. What would I do 
                                with that 30 minutes of free time? Now, here comes the other tug and pull, spend 
                                time connecting with the man I’ll spend forever with, or sit on the couch and watch 
                                mindless television? Tug. Pull. Me Time? Us time? Finding that perfect balance in 
                                motherhood? Is there such a thing?
                            </p>
                            <h2>And, wow, does that tug and pull really get ya when you have to work.</h2>
                            <p>That mom guilt sets in so bad when you have to choose every day whether 
                                to be with your kids or be away from them. Some moms feel this Every. 
                                Single. Day. I’m missing school drop off, but because I did, I kicked 
                                ass and took some names at work.
                            </p>
                            <p>Can we talk about the mom guilt for just a second here? Because it 
                                might be the biggest tug and pull you’ll ever experience as a mom. 
                                You’ll find yourself out with girlfriends, having fun, but simultaneously, 
                                feeling bad that you missed bedtime. You’ll find yourself looking at the 
                                calendar and counting the days that you will be gone in the evenings. Work 
                                event on Monday. Check. Friends dinner on Tuesday. Check. Dinner with hubs on 
                                Thursday. Check. Ugh…  How will they feel if I miss bedtime three nights in a 
                                week? As if, they truly understand calendars.
                            </p>
                            <h2>Tug. Pull…</h2>
                            <p>Most who aren’t mothers see us keeping it together, or maybe doing a bang up 
                                job of raising a fam, but what they don’t see is this constant conflict we 
                                deal with every freaking day. Even those Moms whose kids are long gone from 
                                the house forget what it was like. They tell you, “enjoy every minute”, but 
                                just like birthing a baby… you forget how hard it really is when it’s happening.
                            </p>
                            <h2>I am TRULY doing my best to soak it all up, to find balance in motherhood.</h2>
                            <p>I desperately want to enjoy every early morning cuddle, shrug off every mess, 
                                giggle at the bath-time splashes, embrace the sleepless nights, be present 
                                with my significant other, actually BE at my career instead of wishing I was 
                                somewhere else, and realize that while I’ll always be a mother, this stage will 
                                pass and I might feel some regret, but that’s okay.
                            </p>
                            <p>Being a mom is the one thing that can make you feel like you are falling apart one 
                                minute and totally together the next. Perhaps this is what the elusive balance in 
                                motherhood actually looks like in real time?
                            </p>
                            <p>Now, you listen to me, Mama. We must embrace the tug and pull. This thing called 
                                motherhood is full of joy and sadness, frustration and ease. Perspective is there 
                                to remind us that without defeat you can’t feel accomplished.
                            </p>
                            <p>And that’s what makes being a mom so damn beautiful.</p>
                        </div>
